# Write a program that prompts the user to enter the base and height of a triangle and returns its area.
print("We are finding the are of Triangle A")
base = input("Enter Triangles A base: ")
base = int (base)
height = input("Enter Triangles A height: ")
height = int(height)
area = base * height * 1/2 
print(f"The area of triangle A = {area}")